# 숫자처리함수
print(abs(-5))
print(pow(4, 2))
print(max(5,12))
print(min(5, 12))
print(round(3.14))
print(round(4.89, 2))

from math import *

print(floor(4.99))
print(ceil(3.1))
print(sqrt(14))

from random import *

print(random()) # 0.0 ~ 1.0 미만의 숫자가 나옴
print(random()*10)

print(int(random()*10)) # 0~10미만의 정수
print(int(random()*10))
print(int(random()*10) + 1)

print(int(random()*45)+1) # 1~45 값

print(randrange(1, 45)) # 1~45 미만의 값

print(randint(1,45)) # 1~45 이하의 임의의 값

